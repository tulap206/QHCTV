-- 1. Create collaborators (ctv) table
CREATE TABLE IF NOT EXISTS collaborators (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ma_so TEXT UNIQUE NOT NULL,
    nickname TEXT NOT NULL,
    address TEXT NOT NULL,
    phone TEXT,
    managing_officer TEXT, -- References user name or username, we'll keep it simple
    lat DOUBLE PRECISION NOT NULL,
    lng DOUBLE PRECISION NOT NULL,
    coverage_radius INT NOT NULL DEFAULT 500, -- in meters, for radar scanning area
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    ghi_chu TEXT
);

-- 2. Create index for performance
CREATE INDEX IF NOT EXISTS idx_collaborators_ma_so ON collaborators(ma_so);

